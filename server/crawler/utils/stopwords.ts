export function isStopword(word: string): boolean {
  if (word.length < 2) return true;
  if (KOREAN_STOPWORDS.has(word)) return true;
  if (/^\d+$/.test(word)) return true;
  // 숫자+한글 단위 조합 (3건, 20명 등)
  if (/^\d+[가-힣]{1,2}$/.test(word)) return true;
  return false;
}

/**
 * 한국어 어미/조사 접미 패턴 — 더 공격적으로 제거
 * 순서가 중요: 긴 패턴을 먼저 매칭
 */
const SUFFIX_PATTERNS = [
  // 3글자 이상 어미
  /입니다$/, /습니다$/, /합니다$/, /됩니다$/, /겠습니다$/,
  /이에요$/, /예요$/, /해요$/, /돼요$/,
  /었습니다$/, /였습니다$/,
  // 연결/종결 어미
  /하면서$/, /하지만$/, /하더라$/, /하니까$/,
  /이라고$/, /이라는$/, /라는$/, /라고$/,
  /에서는$/, /에서도$/, /으로는$/, /으로도$/,
  /에서$/, /으로$/, /에게$/, /한테$/, /부터$/, /까지$/, /처럼$/,
  /이다$/, /이고$/, /이며$/, /이라$/, /이든$/,
  /하는$/, /되는$/, /있는$/, /없는$/, /했던$/, /됐던$/,
  /하여$/, /하며$/, /해서$/, /해도$/,
  // 1글자 조사 (마지막에)
  /에$/, /의$/, /를$/, /을$/, /는$/, /은$/, /가$/, /이$/, /와$/, /과$/,
  /도$/, /만$/, /로$/, /서$/, /며$/,
];

/**
 * 어미 패턴으로 흔히 끝나는 동사/형용사/서술어를 추가 필터링
 * 이것은 조사 제거 후에도 남아있는 서술형 단어를 잡아냄
 */
const VERBAL_SUFFIX_PATTERNS = [
  /합니$/, /합니다$/, /합디$/,
  /입니$/, /입니다$/,
  /습니$/, /습니다$/,
  /됩니$/, /됩니다$/,
  /어요$/, /아요$/, /에요$/, /예요$/,
  /세요$/, /셔요$/, /시오$/, /십시오$/,
  /는지$/, /은지$/, /ㄴ지$/,
  /지만$/, /니까$/, /면서$/, /더니$/,
];

function looksLikeVerbalEnding(word: string): boolean {
  return VERBAL_SUFFIX_PATTERNS.some(p => p.test(word));
}

/**
 * 한국어 텍스트에서 의미있는 키워드를 추출하는 토크나이저
 *
 * 전략:
 * 1. 한글 연속 문자열 추출 (2글자 이상)
 * 2. 조사/어미 패턴을 접미에서 제거 (다단계)
 * 3. 불용어 필터링
 * 4. 서술형 잔여물 필터링
 * 5. 영문 키워드도 추출 (브랜드명 등)
 */
export function tokenizeKorean(text: string): string[] {
  const tokens: string[] = [];

  // 전처리: 숫자+한글 합성어를 분리
  // "3억광진구" → "3억 광진구", "5분도보" → "5분 도보", "227호" → "227 호"
  const preprocessed = text
    .replace(/(\d+)([가-힣])/g, '$1 $2')  // 숫자 뒤 한글 → 공백 삽입
    .replace(/([가-힣])(\d+)/g, '$1 $2'); // 한글 뒤 숫자 → 공백 삽입

  // 한글 단어 추출 (2글자 이상 연속 한글)
  const koreanWords = preprocessed.match(/[가-힣]{2,}/g) || [];

  // 단위 명사 (숫자 뒤에 붙는 1글자 한글 — 이미 분리했으므로 제거 대상)
  const unitNouns = new Set([
    '억', '만', '천', '백', '십', '원', '호', '층', '동', '번',
    '분', '초', '시', '년', '월', '일', '개', '명', '건', '대',
    '평', '㎡', '실', '세', '배', '곳', '채', '석', '위',
  ]);

  for (const word of koreanWords) {
    // 단위 명사로 시작하는 합성어를 반복 제거: "만원오피스텔" → "원오피스텔" → "오피스텔"
    let cleaned = word;
    while (cleaned.length >= 3 && unitNouns.has(cleaned[0])) {
      cleaned = cleaned.slice(1);
    }

    // 다단계 접미 제거 (최대 2번)
    for (let pass = 0; pass < 2; pass++) {
      let stripped = false;
      for (const pattern of SUFFIX_PATTERNS) {
        if (cleaned.length > 2 && pattern.test(cleaned)) {
          const result = cleaned.replace(pattern, '');
          if (result.length >= 2) {
            cleaned = result;
            stripped = true;
            break;
          }
        }
      }
      if (!stripped) break;
    }

    // 필터: 불용어
    if (isStopword(cleaned)) continue;
    // 필터: 서술형 어미가 남아있는 단어
    if (looksLikeVerbalEnding(cleaned)) continue;
    // 필터: 너무 짧은 결과 (1글자)
    if (cleaned.length < 2) continue;

    tokens.push(cleaned);
  }

  // 영문 키워드 추출
  const englishWords = text.match(/[a-zA-Z]{2,}/g) || [];
  const englishStopwords = new Set([
    'the', 'be', 'to', 'of', 'and', 'in', 'that', 'have', 'it', 'for',
    'not', 'on', 'with', 'he', 'as', 'you', 'do', 'at', 'this', 'but',
    'his', 'by', 'from', 'they', 'we', 'her', 'she', 'or', 'an', 'will',
    'my', 'one', 'all', 'would', 'there', 'their', 'what', 'so', 'up',
    'out', 'if', 'about', 'who', 'get', 'which', 'go', 'me', 'when',
    'can', 'no', 'just', 'him', 'how', 'its', 'let', 'say',
    'was', 'were', 'been', 'has', 'had', 'are', 'is', 'am',
    'also', 'than', 'other', 'into', 'some', 'could', 'them', 'then',
    'these', 'two', 'may', 'each', 'make', 'like', 'been', 'its',
    'div', 'span', 'class', 'src', 'alt', 'href', 'img', 'var', 'function',
    'return', 'null', 'undefined', 'true', 'false', 'new', 'type',
    'https', 'http', 'www', 'com', 'org', 'net', 'html', 'css', 'js',
    'none', 'auto', 'flex', 'block', 'inline', 'hidden', 'relative',
    'absolute', 'fixed', 'static', 'width', 'height', 'margin', 'padding',
    'color', 'background', 'font', 'text', 'display', 'position',
    'content', 'data', 'item', 'list', 'value', 'name', 'index',
  ]);

  for (const word of englishWords) {
    const lower = word.toLowerCase();
    if (!englishStopwords.has(lower) && lower.length >= 3) {
      tokens.push(lower);
    }
  }

  return tokens;
}
